class Solution:
    def maximumUniqueSubarray(self, nums: list[int]) -> int:
        l, r, num, cur = 0, 0, set[int](), 0
        res = 0
        while r < len(nums):
            if nums[r] not in num:
                cur += nums[r]
                num.add(nums[r])
                r += 1
                res = max(res, cur)
            else:
                cur -= nums[l]
                num.remove(nums[l])
                l += 1
        
        return res 

